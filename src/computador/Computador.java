package computador;

/**
 * Classe que representa um objeto do tipo computador.
 */
public class Computador{

    private String nome;

    /**
     * Construtor de um objeto do tipo computador.
     */
    public Computador(){
        nome = "Caio";
    }

    /**
     * @return Metodo retorna o nome do computador.
     */
    public String getNome() {
        return nome;
    }

}
